char	*ft_strcpy(char *dest, char *src)
{
	int	i;
	int	n;

	n = 0;
	i = 0;
	while (src[n])
	{
		n++;
	}
	i = 0;
	while (i < n)
	{
		dest[i] = src[i];
		i++;
	}
	if (i == n)
		dest[i] = '\0';
	return (dest);
}
