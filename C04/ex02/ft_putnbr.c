#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		b;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		nb = nb * (-1);
		write(1, "-", 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	b = nb % 10;
	b = '0' + b;
	write(1, &b, 1);
}
