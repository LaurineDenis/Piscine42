int		ft_is_prime(int nb)
{
	if ((nb > 2 && nb % 2 == 0) || (nb > 5 && nb % 5 == 0))
		return (0);
	if ((nb > 3 && nb % 3 == 0) || (nb > 7 && nb % 7 == 0) || (nb > 9 && nb % 9 == 0))
		return (0);
	if (nb <= 1)
		return (0);
	return (1);
}

int main(int argc, char **argv)
{
	printf("%d", ft_is_prime(atoi(argv[1])));
}
