int		ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	while (nb > 1)
	{
		nb--;
		i = i * nb;
	}
	if (nb == 0)
		i = 1;
	if (nb < 0)
		i = 0;
	return (i);
}
